export const isDatasetValid = (dsName: string) : boolean => {
  const DsNamePattern = "^[a-zA-Z#$@][a-zA-Z0-9#$@-]{0,7}([.][a-zA-Z#$@][a-zA-Z0-9#$@-]{0,7}){0,21}$";
  const regEx = new RegExp(DsNamePattern);
  return regEx.test(dsName);
}